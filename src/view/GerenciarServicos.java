    private void bttCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttCadastrarActionPerformed
        ManipularServico cadastro = new ManipularServico();
        cadastro.show();

        cadastro.bttSalvar.addActionListener((ActionEvent e) -> {
            boolean unique = true;

            for (Servico s : servicos) {
                if (s.getNome().equals(cadastro.txtNome.getText())) {
                    unique = false;
                }
            }

            if (unique) {
                Servico servico = new Servico(cadastro.txtNome.getText(),
                        cadastro.txtPreco.getText());
                ServicoDAO dao = new ServicoDAO();
                servico.setId(dao.insert(servico));
                servicos.add(servico);
                table.addRow(new Object[]{servico.getNome(),
                    String.valueOf(servico.getPreco())});

                cadastro.dispose();

            } else {
                String msg = "O serviço " + cadastro.txtNome.getText()
                        + " já está cadastrado.\nImpossível cadastrar"
                        + " outro serviço com o mesmo nome.";
                JOptionPane.showMessageDialog(cadastro, msg,
                        "Serviço já cadastrado", JOptionPane.ERROR_MESSAGE);
                cadastro.txtNome.setText("");
            }
        });
    }//GEN-LAST:event_bttCadastrarActionPerformed

    private void bttEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttEditarActionPerformed
        Servico servico = servicos.get(position);
        ManipularServico cadastro = new ManipularServico();
        cadastro.show();
        cadastro.txtNome.setText(servico.getNome());
        cadastro.txtPreco.setText(String.valueOf(servico.getPreco()));

        cadastro.bttSalvar.addActionListener((ActionEvent e) -> {
            if (!servico.getNome().equals(cadastro.txtNome.getText())
                    || !String.valueOf(servico.getPreco())
                            .equals(cadastro.txtPreco.getText())) {
                
                boolean unique = true;
                servico.setNome(cadastro.txtNome.getText());
                servico.setPreco(cadastro.txtPreco.getText());

                for (Servico s : servicos) {
                    if (s.getNome().equals(cadastro.txtNome.getText())
                            && s.getId() != servico.getId()) {
                        unique = false;
                    }
                }

                if (unique) {
                    ServicoDAO dao = new ServicoDAO();
                    dao.update(servico);
                    servicos.set(position, servico);
                    table.setValueAt(servico.getNome(), position, NOME);
                    table.setValueAt(String.valueOf(servico.getPreco()),
                            position, PRECO);
                    cadastro.dispose();

                } else {
                    String msg = "O serviço de " + cadastro.txtNome.getText()
                            + " já está cadastrado.\nImpossível cadastrar"
                            + " outro serviço com o mesmo nome.";
                    JOptionPane.showMessageDialog(cadastro, msg,
                            "Serviço já cadastrado", JOptionPane.ERROR_MESSAGE);
                    cadastro.txtNome.setText("");
                }
            } else {
                String msg = "Não houve alteração no serviço de "
                        + cadastro.txtNome.getText() + ".";
                JOptionPane.showMessageDialog(cadastro, msg,
                        "Alteração não salva", JOptionPane.INFORMATION_MESSAGE);
            }
        });
    }//GEN-LAST:event_bttEditarActionPerformed

    private void bttExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttExcluirActionPerformed
        String msg = "Você está prestes a excluir o serviço de "
                + servicos.get(position).getNome() + "!"
                + "\nDeseja mesmo excluí-lo?";

        int res = JOptionPane.showConfirmDialog(
                rootPane, msg, "Excluindo serviço",
                JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);

        if (res == JOptionPane.YES_OPTION) {
            ServicoDAO dao = new ServicoDAO();
            dao.delete(servicos.remove(position));
            table.removeRow(position);
            bttEditar.setEnabled(false);
            bttExcluir.setEnabled(false);
        }
    }//GEN-LAST:event_bttExcluirActionPerformed

    private void bttProcurarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttProcurarActionPerformed
        if (txtProcurar.getText().trim().isEmpty()) {
            if (subList) {
                ServicoDAO dao = new ServicoDAO();
                servicos = dao.queryAll();
                inflateTable();
                subList = false;
            }
            txtProcurar.setText("");

        } else {
            ServicoDAO dao = new ServicoDAO();
            servicos = dao.queryForString(txtProcurar.getText());
            inflateTable();
            subList = true;
        }
    }//GEN-LAST:event_bttProcurarActionPerformed

