    private void tblServicosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblServicosMouseClicked
        position = tblServicos.getSelectedRow();

        if (position != -1) {
            bttEditar.setEnabled(true);
            bttExcluir.setEnabled(true);
        } else {
            bttEditar.setEnabled(false);
            bttExcluir.setEnabled(false);
        }
    }//GEN-LAST:event_tblServicosMouseClicked

    protected void inflateTable() {
        String col[] = {"Nome", "Preço"};
        table = new DefaultTableModel(col, 0) {
            boolean[] canEdit = new boolean[] {false, false};

            @Override
            public boolean isCellEditable(int row, int column) {
                //return super.isCellEditable(row, column);
                return canEdit[column];
            }
        };
        tblServicos.setModel(table);

        servicos.forEach((servico) -> {
            table.addRow(new Object[] {
                servico.getNome(),
                String.valueOf(servico.getPreco())
            });
        });

        bttEditar.setEnabled(false);
        bttExcluir.setEnabled(false);
    }

